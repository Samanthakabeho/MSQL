CREATE TABLE FARMERS(
    FARMERID INT,
    FARMERNAME VARCHAR(45),
    FARMER_ADDRESS VARCHAR(45),
    FARMER_PHONE VARCHAR(40),
    FARMER_EMAIL VARCHAR(35),
    PRIMARY KEY(FARMERID)
);

CREATE TABLE FARM(
    FARMID INT,
    FARMERID INT,
    FARMNAME VARCHAR(50),
    FARMLOCATION VARCHAR(50),
    FARMSIZE DECIMAL(10,2),
    PRIMARY KEY(FARMID),
    FOREIGN KEY(FARMERID)REFERENCES FARMERS(FARMERID)
);
CREATE TABLE CROP(
    CROPID INT,
    CROPNAME VARCHAR(50),
    CROPTYPE VARCHAR(50),
    PRIMARY KEY(CROPID)
);
CREATE TABLE SEASON(
    SEASONID INT,
    SEASONNAME VARCHAR(50),
    STARTDATE DATE,
    ENDDATE DATE,
    PRIMARY KEY(SEASONID)
);
CREATE TABLE HARVEST(
    HARVESTID INT,
    FARMID INT,
    CROPID INT,
    SEASONID INT,
    HARVESTDATE DATE,
    YIELD DECIMAL(10,2),
    PRIMARY KEY(HARVESTID),
    FOREIGN KEY (FARMID)REFERENCES FARM(FARMID),
    FOREIGN KEY(CROPID)REFERENCES CROP(CROPID),
    FOREIGN KEY(SEASONID)REFERENCES SEASON(SEASONID)
);
DESC farmers;
